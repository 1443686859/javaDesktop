class Solution {
    public int jump(int[] nums) {
        int index = 0;
        int result = 0;
        int n = nums.length;
        while (index < n - 1) {
            int max = index + nums[index];
            if (max >= n - 1) {
                result++;
                break;
            }
            int maxIndex = index + 1;
            for (int i = index + 1; i <= max; i++) {
                if (nums[maxIndex] + maxIndex <= i + nums[i]) {
                    maxIndex = i;
                }
            }
            result++;
            index = maxIndex;
        }
        return result;
    }
}
