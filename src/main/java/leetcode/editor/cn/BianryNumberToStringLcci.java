class Solution {
    public String printBin(double num) {
        StringBuilder result = new StringBuilder("0.");
        int i = 0;
        while (i <= 30) {
            if (num == 0) {
                break;
            }
            num *= 2;
            if (num > 1) {
                result.append("1");
                num -= 1;
            } else {
                result.append("0");
            }
            i++;
        }
        if (num != 0) {
            return "ERROR";
        }
        return result.toString();
    }
}
