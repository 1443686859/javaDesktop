/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean isCousins(TreeNode root, int x, int y) {
        int[] parent = new int[101];
        Arrays.fill(parent, - 1);
        int[] index = new int[101];
        dfs(parent, index, root, -1, 0);
        return index[x] == index[y] && parent[x] != parent[y];
    }
    public void dfs(int[] parent, int[] d, TreeNode root, int p, int index) {
        if (root == null) {
            return ;
        }
        parent[root.val] = p;
        d[root.val] = index;
        dfs(parent, d,root.left, root.val, index + 1);
        dfs(parent, d, root.right, root.val, index + 1);
    }
}
